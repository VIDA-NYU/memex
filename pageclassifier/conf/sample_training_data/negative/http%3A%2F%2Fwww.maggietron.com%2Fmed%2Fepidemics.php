<b>Black Death and Leprosy</b>

<p>The two most curious epidemics during the Middle Ages were Black Death and leprosy.  Due to the specific environmental circumstances of medieval Europe and the religion of medieval people, these two epidemics had great social repercussions.  

<p>&nbsp;

<p><b>Black Death</b>

<p>In early 1347, a fearful epidemic of bubonic plague broke out in Constantinople.  From then on, this great plague would reach Europe and kill approximately from one-fourth to nine-tenths of the human population in the affected areas.

<p>What is the Black Death, also known as the Black Plague, Bubonic Plague, or just the Plague?  From a modern medical point of view, it is a pneumonic type of an infection, highly contagious, which could be transmitted via inhalation, ingestion, or even slight abrasion of skin.  Usually, lung lesions occur, hearth and kidneys turn into fatty goo, and death may occur from hearth failure.  The walls of blood vessels are attacked frequently causing hemorrhages and acute blood poisoning.  It is fatal in almost all cases.

<p>Why such name, Black Death?  "The traditional belief is that it was so called because the putrefying flesh of the victims blackof blood vessels are attacked frequently causing hemorrhages and acute blood poisoning.  It is fatal in almost all cases.

<p>Why such name, Black Dened in the final hours before death supervened.  The trouble about this otherwise plausible theory is that no such phenomenon occurred.  It is true that, in cases of septicemic plague, small black or purple blotches formed on the bodies of the sick and this symptom must have made a vivid impression on beholders" (Ziegler 7).

<p>The position of a medieval doctor faced with Black Death was that of certainty that the air surrounding the infected area is at fault.  Because the Plague would attack a particular region, kill off everyone within it, and then move on to an adjacent region, the circulating and moving air was blamed for the deaths.  The idea of the infection of the atmosphere dates back to Galen;  what is shocking is the fact that no medieval doctor formed a logical explanation for Black Death, which would interfere with the poisoned atmosphere theory.  Details of the symptoms were gathered by many in literary forms, yet surprisingly, nobody bothered to put together all the information and logically analyze the occurrences at hand.  

<p>Eventually, not being able to deny the evidence of their own eyes, people started to make a connection between an infected person and the increasing number of the infected around that person.  Given the disgusting nature of the disease itself and its apparent infectiousness, the infected individuals instead of ren eyes, people started to make a connection between an infected person and the increasing number of the infected around that person.  Given the disgusceived pity were ostracized.  During the later months of Black Death in Europe, it was common to see unburied corpses piled on top of each other in dug up holes.

<p>In addition to actual contact with an infected person, rats and fleas played a role in spreading the disease.  The rats were basically used as transport for the fleas who would not mind living partially on humans, partially on rats.  This way, enough bacteria from one person could be easily transported to another by the means of a flea.  The rats provided for easier spread of the disease on a larger geographical scale.

<p>No cure has been found for the Plague in the Middle Ages, although on occasion, the more glory-seeking doctors would visit hospitals or the sick in other areas and speak up their thoughts on this illness, mostly in order to have their voice heard and obtain fame within the medical world. 

<p>&nbsp;

<p><b>Leprosy</b>

<p>Leprosy is an infectious disease caused by <i>Mycobacterium leprae</i>, a first cousin of tuberculosis bacteria.  Unlike tuberculosis, leprosy bacteria cannot be grown outside living animal cells, and even within them they multiply very slowly;  they can remain dormant, alive but inactive, for a long time.  Leprosy bacteria probably spread from person to person as readily as tuberculosis, but disease less often follows, both because the bacteells, and even within them they multiply very slowly;  they can remain dormant, alive but inactive, for a long time.  Leprosy bacteria probably spread from pria are less virulent and because most people have a high degree of natural resistance to the disease.

<p>Why then, such panic about it during the Middle Ages?  First of all, as scary as leprosy sounds, we cannot be sure that what is described as leprosy was in fact this particular disease in the Middle Ages.  It seems that this name was given oftentimes to many other disfiguring diseases.  This is a great example of the underlying principles of medieval European world; more often than not, in cases of disfigurement it was not the actual educated doctors who would diagnose a patient, but rather it would be the priests or even the common people who would become the arbiters of the disease.  The cure for leprosy was not known and the treatment prescribed was usually isolation.

<p>Lepers were usually banned to leper hospitals or leper communities.  "Throughout medieval Europe admission fees, gifts, tolls, and taxes helped to support leper hospitals, although their wealth depended mainly upon endowments" (Richards 33).  In many cases, leper hospitals housed as well the poor and other sick, who were not fearful enough of leprosy but hungry enough to risk infection.  

<p>In many cases, leprosy would mean separation from family, from husband or wife.  Some European countries would allow the spouse to join the leper or divorce may be a solution.  This was arful enough of leprosy but hungry enough to risk infection.  

<p>In many cases, leprosy would mean separation from family, from husband or wife.  S difficult decision, for under law, a leper held no rights, and under Church doctrines, a leper was considered dead.  A spouse of a leper had to decide whether to abandon his or her chosen life partner or join them in non-existence.  Additionally, if a wife and husband decided to leave together, they might have a hard time finding a leper colony that would accept both males and females, as such colonies were usually gender biased.

<p>A leper in a leper colony would more often than not suffer from depression due to sudden abandon of known circle of family and friends, and the new unfamiliar surroundings.  In addition, a leper, as opposed to any other sick person in the Middle Ages, could not expect visits, for leprosy was thought to be extremely contagious.

<p>With time, leper colonies and hospitals suffered a decline.  In the early 16th century, the leper population was practically nonexistent.  This is mostly due to Black Death, which has killed a great portion of the population, including the already sick (and possibly more prone to the disease) lepers.  
















