<H3>Access Excellence Classic Collection</H3>
<H1>Development of Polio Vaccines</H1>
<p>

by Bonnie A. Maybury Okonek and Linda Morganstein, editor

<P>
<hr>

<p>

In 1921, outbreaks of poliomyelitis plagued America. That summer, a young politician named Franklin Delano Roosevelt was vacationing with his family at their Campobello estate. After an exhausting day fighting a local forest fire, taking a cold swim for relief, and then lounging in his wet swimsuit at home, he went to bed feeling as though he had contracted a cold. In a few days Roosevelt found out he had polio.
<p>	As it did with Roosevelt, polio can strike quickly. The virus enters the body by nose or mouth and travels to the intestines, where it incubates. A few days later, most patients are either asymptomatic or they experience flu-like symptoms, such as headache, nausea, vomiting, and fever. Whether they are symptomatic or not, people at this stage can pass the disease on to others. Polio can be spread through contact with infected feces or through infected droplets traveling through the air, in food, or in water. The virus next enters the bloodstream, and the patient makes antibodies against it. In most cases, this stops the progression of the virus; lifelong immunity against the disease is acquired. 10% of infected people develop symptoms and 1% develop the paralytic form of polio.
<p>
<table align=right hspace=3>
<tr>
<td align=center><img src=images/MedCtr_sm.gif height=141 width=218></td>
</tr>
<tr>
<td><font color=gray size=-1>Rancho Los Amigos Medical Center.<br>Downey, CA, 1952.</font></td>
</tr>
</table>
<p>	Roosevelt was one of the unlucky ones. His legs were left permanently paralyzed. In cases such as his, the virus reaches the brain and spinal cord where it multiplies and destroys the nerve tissue. At this point the disease becomes spinal or bulbar (involving the last four or five cranial nerves), depending on which nerves are affected. Both forms are characterized by muscle pain, stiff neck and back, and possible paralysis. The spinal form affects the limbs. The bulbar form affects the lungs so that patients cannot breathe. After a severe attack of polio in its paralytic form, there is no treatment for the disease itself, although symptoms such as muscular paralysis can be helped with physical therapy. How much a person will recover varies from individual to individual.

<p>
Roosevelt was determined not to let this major illness get the best of him. He not only continued his illustrious political career, resulting in his well-documented and long term Presidency of the United States, but he went on to spearhead the fight against polio, increasing public awareness of the deadly disease and promoting research. Although polio never devastated large numbers of the population like the plague or influenza, it was a frightening, highly contagious disease that attacked both the poor and rich and arose in terrifying outbreaks which seemed impossible to stop in spite of advances in medicine.
<p>	Through the first half of this century, basic hygiene methods and knowledge had advanced tremendously. For the first time in civilization, people came to expect good health instead of merely hoping for it. But polio still attacked, and children were the most vulnerable. Many can remember seeing, in the early 1950s, heartbreaking posters of children on crutches or in iron lungs, the cumbersome mechanical aids which helped those whose lungs were paralyzed to breathe. Lacking a vaccine, parents throughout the country panicked, keeping their children from schools and other public facilities. Why couldn�t a vaccine be found?
	
<h3>How a Vaccine Was Discovered</h3>

	Vaccine developments for polio had begun in the early 1900s. However, early attempts failed, partly because researchers did not know there was more than one virus. We now know that polio is caused by three strains of quite stable viruses that are a part of the enterovirus family, which have RNA as their genetic material. These viruses can remain infectious for long periods of time in water and foods. To be effective, a vaccine has to confer immunity against all three strains.
<p>	Ironically, before the 1900s, immunity was acquired primarily during infancy because sanitation conditions were poor and efforts at sewage and water treatment were primitive. Babies were frequently exposed to polioviruses. These infants did not contract the disease because their mot, immunity was acquired primarily during infancy because sanitation conditions were poor and efforhers� antibodies were passed on to them through breast feeding. The babies then developed their own antibodies to the virus. 
<p>	Paradoxically, when sanitation improved, infants were no longer exposed at an age when they were protected, so they did not develop antibodies to the viruses. Consequently, when they were exposed to the virus in later childhood and adulthood, they were at risk to contract polio.
<p>	This understanding about the nature of polio grew through painstaking research over the first half of the twentieth century, much of it funded by the March of Dimes, a grassroots organization founded with the help of President Roosevelt. The organization decided to enlist the services of a respected researcher who was certain he could find a safe vaccination. 
<p>
<table align=left hspace=3>
<tr>
<td align=center><img src=images/JonasSalk_sm.gif height=188 width=183></td>
</tr>
<tr>
<td><font color=gray size=-1>Jonas Salk, developer of first successful vaccine.<br>Photo by March of Dimes<br>Birth Defects Foundation, 1996. </font></td>
</tr>
</table>
<p>	Dr. Jonas Salk had begun his medical research career studying immunology. In 1947, while at the University of Pittsburgh, he began his research on poliovirus. His research was greatly helped in 1949, when a method of growing poliovirus in cell culture, instead of having to use primarily monkeys for research, was discovered. Salk needed to find a way to process the viruses so that they were less infectious, before using them in a vaccine. In 1952, Salk was the first to develop a successful vaccine using a mixture of the three types of virus, grown in monkey kidney cultures. He developed a process using formalin, a chemical that inactivated the whole virus. 
<p>	What followed was  massive testing of the vaccine in clinical trials in the United States and parts of Canada, begun in 1954. The scope of the trials was unprecedented in medical history. The results were dramatic. Cases of polio fell spectacularly in the vaccinated test groups. In 1955, the government quickly granted permission for the vaccine to be distributed to the children of our country.
<p>
<table align=right hspace=3>
<tr>
<td align=center><img src=images/childshot_sm.gif height=152 width=141></td>
</tr>
<tr>
<td><font color=gray size=-1>Child being administered the polio vaccine.<br>Photo by March of Dimes<br>Birth Defects Foundation, 1996.</font></td>
</tr>
</table>
<p>	But, there was a problem with the original Salk vaccine. The vaccine actually induced 260 cases of poliomyelitis, including 10 deaths. The problem was traced to incomplete inactivation of some virus particles, which was soon corrected. Since then the vaccine has been highly effective, with a 70 - 90% protection rate. 

<p>	In 1957, in an effort to improve upon the killed Salk vaccine, Albert Bruce Sabin began testing a live, oral form of vaccine in which the infectious part of the virus was inactivated (attenuated). This vaccine became available for use in 1963. 
<br clear=right>
<h3>The Salk and Sabin Vaccines Today</h3>
	
	The Salk vaccine is given in two intramuscular injections spaced one month apart and requires boosters every 5 years. Because of the way it is inactivated, the vaccine is safe for those with compromised (weakened) immune systems. 
<p>
<table align=left hspace=3>
<tr>
<td algin=center><img src=images/immunization_sm.gif height=160 width=218></td>
</tr>
<tr>
<td><font color=gray size=-1>Polio immunization at Cutter Lab. Berkeley, CA, 1955.<br>Photo by March of Dimes Birth Defects Foundation, 1996. </font></td>
</tr>
</table>
<p>	The Sabin oral vaccine is given in 3 doses in the first two years of life, and a booster is given when the child starts school. Further boosters are not given unless the patient is exposed to polio or will be traveling to an endemic region. The advantages of a live, oral vaccine are its long-lasting immunity, the prevention of reinfection of the digestive tract, and the lower cost of administering the vaccine orally because sterile syringes and needles are not necessary. However, a major disadvantage is that it cannot be used for patients with compromised immune systems because it is a live virus and can cause disease in these patients. It also cannot be used by those in close contact with immunocompromised patients because the live virus in the vaccine can be shed in the feces of those who ingest it, and can possibly be transmitted to the immunocompromised patient. Another disadvantage of the Sabin oral vaccine is that those who have an enterovirus infection of the gastrointestinal tract when taking the oral vaccine may not develop the immune response. Clearly, both vaccines have their advantages and disadvantages with regard to relative safety and cost.
<p>	The debate between safety and cost will continue, but we are fortunate to have two good alternatives to choose from. Both vaccines are currently in use throughout the world. In the United States, the Sabin vaccine is used almost exclusively. In other countries, the Salk vaccine is preferred. Research continues to improve these vaccines. More effective culturing and purification techniques have been developed,ghout the world. In the United States, the Sabin vaccine is used almost exclusively. In other countries, the Salk vaccine is preferred. Research continues to improve allowing the vaccines to induce higher levels of antibody formation. 
<p>	In the exciting research field of recombinant biotechnology, scientists are also attempting genetic alteration of the poliovirus. Researchers are using Escherichia coli (a common bacterium that inhabits the gastrointestinal tract of humans) as a host for bacterial gene cloning. Work is being done to take the genes of poliovirus which code for the synthesis of the viral capsid (the protein coat of a virus particle) and to combine it with E.coli�s genes. The E.coli can then synthesize viral capsid proteins to be used in making a vaccine. This latter approach eliminates any possibility of the virus infecting the vaccinated patient because the vaccine contains only a part of the virus, excluding potentially dangerous content.

<h3>World Health and the Eradication of Polio</h3>

	The discovery and use of polio vaccines has all but eliminated polio in the Americas. In 1960, there were 2,525 cases of paralytic polio in the United States. By 1965, there were 61. Between 1980 and 1990, cases averaged 8 per year, and most of those were induced by vaccination! There has not been a single case of polio caused by the wild virus since 1979, with a rare case reported each year from persons coming into the country carrying the virus. In 1994, polio was declared eradicated in all of the Americas.
<p>	In 1988, the World Health Organization set a goal of eradication of poliomyelitis from the entire world by the year 2000. This is theoretically possible since the poliovirus is found only in humans, and humans can be immunized. Smallpox was the first disease in history to be eradicated. It seems likely that polio could follow in its footsteps.

