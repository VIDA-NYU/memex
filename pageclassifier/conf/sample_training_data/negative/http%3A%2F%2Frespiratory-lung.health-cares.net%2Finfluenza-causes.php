                  <td><h1>What causes influenza?</h1>                    
                    The flu is caused by three types (strains) of viruses - influenza A, B and C. Type A is responsible for the deadly influenza pandemics (worldwide epidemics) that strike every 10 to 40 years, whereas type B causes smaller, more localized outbreaks. Type C is less common and causes only mild symptoms. The influenza virus is generally passed from person to person by airborne transmission (i.e., sneezing or coughing). But, the virus can also live for a short time on objects -- such as doorknobs, pens, pencils, keyboards, telephone receivers, and eating or drinking utensils. Therefore, it may also be spread by touching something that has been handled by someone infected with the virus and then touching your own mouth, nose, or eyes Influenza virus is spread by inhaling droplets that have been coughed or sneezed out by an infected person or by having direct contact with an infected person's secretions. Handling household articles that have been in contact with an infected person or an infected person's secretions may sometimes spread the disease.</td>
