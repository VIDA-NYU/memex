                  <td><h2>More information on influenza (flu)</h2>
<a href="http://respiratory-lung.health-cares.net/influenza.php">What is influenza (flu)?</a> - Influenza (flu) is a respiratory infection caused by one of the influenza viruses that typically is spread by air or by direct contact.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-transmission.php">How is influenza transmitted?</a> - Influenza is spread through contact with droplets from the nose and throat of an infected person during coughing and sneezing.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-causes.php">What causes influenza?</a> - The flu is caused by three types (strains) of viruses - influenza A, B and C. Influenza A is responsible for the deadly influenza pandemics.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-virus.php">What's the influenza virus?</a> - The influenza virus is in a class of viruses known as orthomyoxoviruses, with myxo referring to the fact that they infect mucus membranes. <br>
<a href="http://respiratory-lung.health-cares.net/influenza-risk-factors.php">What're the risk factors for influenza?</a> - All people 50 years of age and older are at increased risk for serious illness with the flu, or in contact with those at high risk, and should receive vaccine.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-complications.php">What're the complications of influenza?</a> - Influenza complications usually arise from bacterial infections of the lower respiratory tract. Pneumonia is the major serious complication of influenza.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-symptoms.php">What are the symptoms of influenza?</a> - Influenza can cause a variety of symptoms. Typical flu symptoms include headache, fever, chills, cough and body aches. Intestinal symptoms are uncommon.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-diagnosis.php">How is influenza diagnosed?</a> - Diagnosis of influenza (flu) is based on typical symptoms of fever, chills, headaches, cough and body aches. There are a variety of tests to detect influenza. <br>
<a href="http://respiratory-lung.health-cares.net/influenza-treatment.php">What is the treatment for influenza?</a> - The main treatment for influenza (flu) is to rest adequately, drink plenty of fluids, and avoid exertion. Amantadine and rimantadine are effective against influenza A.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-prevention.php">How to control or prevent influenza?</a> - The preferred treatment for influenza, and most viral infections is prevention. The prevention can be achieved through vaccinations.<br>
<a href="http://respiratory-lung.health-cares.net/influenza-vaccine.php">What's the influenza vaccine (flu vaccine)?</a> - Vaccination is the principal measure for preventing influenza. Influenza vaccinations are given to millions annually.</td>
