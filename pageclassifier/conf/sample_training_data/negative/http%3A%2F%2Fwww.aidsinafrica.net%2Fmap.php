                           <p>Sub-Saharan Africa is home to over 70% of the total world HIV-positive population.
               In the southernmost region
               we find countries with HIV prevalence rates of over 30%, the
               highest in the world. There is no overriding explanation for why HIV has exploded in some areas and not others. 
               We tend to draw over-simplistic correlations between HIV/AIDS and 
               demographics such as wealth, literacy,  or fertility. This map allows you to view
               HIV prevalence along with other forms of demographic data to
               illustrate that many of these generalizations do not hold true.
               </p>
              <p>Identifying what causes HIV to spread is a complicated and growing area of research.
                Specific factors that seem to play a role include patterns of sexual 
                networking, levels of condom use with different partners, incidence 
                of other sexually transmitted diseases, population mobility, and 
                societal make-up. While over 90% of the world's HIV+ population live in poorer developing countries, 
                some of the more wealthy countries, such as Botswana and South Africa, have higher HIV rates.
                This is in part because wealth facilitates population mobility, therefore increasing the spread of HIV.
                </p>
              <p>Joint United Nations Programme on HIV/AIDS (UNAIDS) and the World Health Organization (WHO)
                release new HIV figures each year.
                This data is uploaded into the AidsinAfrica.net database as soon as it is made available.
                Along with the most recent HIV statistics, the database holds HIV prevalence
                information by country dating back to 1970. The historic estimates are used
                to display the animation of HIV spreading across the continent over time. This animation will increase
                in length each year as more data is added.
              </p>
                <p>When looking at HIV statistics, one should remember that a
                person can be HIV-positive (HIV+) and show no symptoms associated 
                with AIDS. It takes between 
                xx months to xx years without antiretroviral treatment
                before an HIV+ person shows signs 
                of their immune system breaking down.
                Therefore, a large portion of a country's
                HIV+ population can be completely healthy.
                When HIV surges in an area, there will
                be a few years delay until people, families, communities, or the healthcare system
                are effected.<br>
                </p> 
