                          <p>Definitions & Sources:
              </p>
              <p><b>HIV Prevalence</b><br>
                HIV prevalence is defined here as the percentage of men and women 
                between the ages of 15 and 49 who are HIV positive.
                UNAIDS and the WHO  
                work with national governments and research institutions to calculate 
                this using a methodology that has thus far proved accurate. 
                Note that the estimates do not claim to be an exact count of infections.<br>
                Source: <a href="http://www.unaids.org">UNAIDS</a>
              </p>
              <p><b>Gross Domestic Product (GDP) per Capita</b><br>
                GDP represents the value of all goods and services produced domestically
                in a country within a year. The average GDP is obtained by dividing the GDP 
                by the population. This figure provides a measurement of average wealth, 
                but can be misleading because a country's wealth is usually distributed 
                very unevenly. GDP and population figures 
                are obtained by the U.S. Census Bureau, International 
                Database and The World Factbook, 2001.<br>
                Source: <a href="http://www.worldbank.org/data/">World Bank Group</a>
              </p>
                <p><b>Fertility</b><br>
                Fertility is a measurement of the &quot;childbearing performance&quot; of a population.
                It is the ratio of live births in an area to the population
                of that area. The numbers are expressed here as per 1000 population per year.<br>
                Source: <a href="">World Health Organization</a>
                </p>
                <p><b>Corruption Perceptions Index (CPI)</b><br>
                The corruption figures here are based on
                &quot;The 2002 Transparency International Corruption Perceptions
                Index.&quot; The index defines
                corruption as the abuse of public office for private gain. 
                It measures CPI by examining how corruption is perceived by business people and country analysts.
                The scale is between 0 and 10, with lower numbers meaning higher perception of corruption.
                Only 102 of the world's countries are surveyed. This study is integrated into the map
                to counteract the generalization that all African countries are highly corrupt.
                Botswana, for example, is ranked between France and Ireland in the CPI study.<br>
                Source: <a href="http://www.infoplease.com/ipa/A0781359.html">InfoPlease</a>
                </p>

