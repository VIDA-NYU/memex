<h2>Religious Interpretations of the Causes of the Plague</h2>
<p>Plague tractates were long on prevention and short on cures. They were used as a strategy to reassure the people that plague did not just happen without cause, and there were countless theories invented to explain the pestilence. (For a more in depth discussion of contents of tractates see Campbell.)</p>
<p>The earliest known tractate is the regimen of the physician James of Agramont written in 1348 to be read to the common people. James pointed out that the plague killed indiscriminately, destroying master and servant alike. Further, he cited Deuteronomy 24, in which God promised prosperity to those who keep his commandments, and plague to those who do not. He noted other Biblical passages which state that plague is also a punishment of the sin of pride.</p>
<p>The Regensburg chronicler Konrad von Megenburg took up the question of whether the sinfulness of humanity caused or was caused by the plague. He concluded that society itself had caused the plague by its sinful behavior. Others had similar sentiments: that the plague was caused by the wickedness of humanity, and that this wickedness was manifested by an assault on the universals that held society together. In addition, plague was a cure for social fragmentation and sin. Boccaccio himself seemed to hold this belief. Boccaccio condemns the people who fled the city in hopes oficke escaping the plague.by an assault on the universals that held society together. In addition, plague was a cure for social fragmentation and sin. Boccaccio himself seemed to hold this belief. Boccaccio condemns the people who fled the city in hopes oficke &quot;It was as though they imagined that the wrath of God would not unleash this plague against men for their iniquities irrespective of where they happened to be...&quot; (McWilliam translation).</p>
<p>One medieval Christian understanding of the Black Death revolved around the Book of Revelation and its notion of the Four Horsemen of the Apocalypse - pestilence, war, famine and death. Christians used this biblical context to rationalize and accept the horrible disease shaking Europe. Others thought that the Plague was a sign that Christ's return to reign over the earth was imminent. Still others blamed prideful women and fraudulent Jews for bringing on the Plague in Europe.</p>
